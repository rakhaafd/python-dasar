# Operator is memeriksa apakah kedua objek itu sama.
a = 1 # int
b = 1.0 # float
print(a == b) # True
print(a is b) # False

# Operator in memeriksa untuk melihat apakah kedua string cocok sebagian.
print('aaa' in 'aaa-bbb-ccc') # True
print('bbb' in 'aaa-bbb-ccc') # True
print('ddd' in 'aaa-bbb-ccc') # False